def max_dragon_power(n):
    """
    Вычисляет максимальную силу драконьей стаи при заданном количестве голов.
    Входные данные: Общее количество голов в стае (0 < n < 100)
    Выходные данные: максимальная сила стаи (произведение числа голов каждого дракона)
    """
    # Базовый случай: если голов 0, сила 0
    if n == 0:
        return 0

    # Создаем массив для хранения максимальных значений силы для каждого количества голов
    dp = [0] * (n + 1)
    dp[0] = 1  # Пустая стая имеет силу 1 (нейтральный элемент умножения)

    # Перебираем все возможные количества голов от 1 до n
    for i in range(1, n + 1):
        # Перебираем возможное количество голов у последнего дракона (от 1 до 7)
        for heads in range(1, 8):
            if i >= heads:
                # Обновляем максимальное значение силы
                dp[i] = max(dp[i], dp[i - heads] * heads)

    return dp[n]


def main():
    try:
        # Ввод данных
        n = int(input("Введите количество голов драконьей стаи (от 1 до 100): "))

        # Проверка диапазона
        if n <= 0 or n >= 100:
            print("Ошибка: количество голов должно быть от 1 до 99.")
            return

        # Вычисление максимальной силы
        result = max_dragon_power(n)

        # Вывод результата
        print(f"Максимальная сила драконьей стаи из {n} голов: {result}")

    except ValueError:
        print("Ошибка: введите целое число.")
    except Exception as e:
        print(f"Произошла ошибка: {e}")


# Запуск программы
if __name__ == "__main__":
    main()